    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String idProveedor = text_01.getText();
        String nombre = text_02.getText();
        String stock =  text_03.getText();
        String precio = text_04.getText();
        
        if(!"".equals(idInsumo) &&
                !"".equals(idProveedor) &&
                !"".equals(nombre) &&
                !"".equals(stock) &&
                !"".equals(precio))
        {
            DAOFactory fact = DAOFactory.getDAOFactory(DAOFactory.MYSQL);
            
            InsumoDAO dao = fact.getInsumoDAO();
            
            try
            {
                dao.update(new DTOInsumo(
                        Integer.parseInt(idInsumo),
                        Integer.parseInt(idProveedor),
                        nombre,
                        Integer.parseInt(stock),
                        Float.parseFloat(precio)));
                
                idInsumo = "";
                
                actualizarInsumos();
            }
            catch(Exception ex)
            {
                
                System.out.println(ex);
                
            }
        }
        else{
            
            System.out.println("Debe ingresar todos los datos");
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

