    void actualizarProveedores(){
        
        DAOFactory fact = DAOFactory.getDAOFactory(DAOFactory.MYSQL);
        
        ProveedorDAO dao = fact.getProveedorDAO();
        
        try{
            List<DTOProveedor> items = dao.read();

            DefaultTableModel model = (DefaultTableModel) tablaProveedores.getModel();
            model.setRowCount(0);

            for(int i = 0; i < items.size(); ++i)
            {
                Object[] row = { 
                    items.get(i).getIdProveedor(),
                    items.get(i).getNombre(),
                    items.get(i).getDireccion(),
                    items.get(i).getDistrito(),
                    items.get(i).getTelefono()
                };    
                model.addRow(row);
            }
        }
        catch(Exception e){
            
            System.out.println(e);
            
        }
        
    }
    
